def repeat(word, repetitions):
    """ (str, int) -> str
    
    Retuns a word or sentance and repeats it the allocated number of times.
    
    >>> repeat('Marcia ', 3)
    Marcia Marcia Marcia
    >>> repeat('Buffalo ',8)
    Buffalo Buffalo Buffalo Buffalo Buffalo Buffalo Buffalo Buffalo 
    """
    return word * repetitions

    
    

